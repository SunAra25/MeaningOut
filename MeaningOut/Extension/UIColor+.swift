import UIKit

extension UIColor {
    // MARK: Primary
    class var meaningPrimary: UIColor? { return UIColor(named: "Primary") }
    
    // MARK: Gray Scale
    class var meaningGray1: UIColor? { return UIColor(named: "Gray1") }
    class var meaningGray2: UIColor? { return UIColor(named: "Gray2") }
    class var meaningGray3: UIColor? { return UIColor(named: "Gray3") }
    
    // MARK: ETC
    class var meaningBlack: UIColor? { return UIColor(named: "Black") }
    class var meaningWhite: UIColor? { return UIColor(named: "white") }
}
