import UIKit

extension UIFont {
    class var headM: UIFont { .systemFont(ofSize: 16) }
    class var headB: UIFont { .systemFont(ofSize: 16, weight: .heavy) }
    class var bodyM: UIFont { .systemFont(ofSize: 15) }
    class var bodyB: UIFont { .systemFont(ofSize: 15, weight: .heavy) }
    class var subM: UIFont { .systemFont(ofSize: 14) }
    class var subB: UIFont { .boldSystemFont(ofSize: 14) }
    class var capM: UIFont { .systemFont(ofSize: 13) }
    class var capB: UIFont { .boldSystemFont(ofSize: 13) }
}
