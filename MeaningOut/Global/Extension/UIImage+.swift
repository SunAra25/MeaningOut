extension UIImage {
    class var magnifyingglass: UIImage { return UIImage(systemName: "magnifyingglass")! }
    class var clock: UIImage { return UIImage(systemName: "clock")! }
    class var person: UIImage { return UIImage(systemName: "person")! }
    class var xmark: UIImage { return UIImage(systemName: "xmark")! }
    class var chevron: UIImage { return UIImage(systemName: "chevron.right")! }
    class var camera: UIImage { return UIImage(systemName: "camera.fill")! }
}

